package bangundatar;

public class Persegi {
    public float sisi;

    public float getSisi() {

        return sisi;
    }

    public void setSisi(float sisi) {

        this.sisi = sisi;
    }
    public float hitungluas() {
        return sisi*sisi;
    }
    public float hitungkeliling() {

        return 4*sisi;
    }
    }



